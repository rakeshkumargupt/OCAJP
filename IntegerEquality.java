public class IntegerEquality {
          public static void main(String[] args) {
                     Integer i = -10;
                     Integer j = -10;
                 System.out.println(i==j);
                 System.out.println(i.equals(j));
   
                     Integer n = -129;
                     Integer m = -129;
                System.out.println(n==m);
                System.out.println(n.equals(m));
}
}